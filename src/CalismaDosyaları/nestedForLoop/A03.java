package CalismaDosyaları.nestedForLoop;

public class A03 {
    public static void main(String[] args) {




        /*
        verilen yukseklik
         ve boy  degerine göre
         yıldızlardan olusan bir
         dikdörtgen olusturalım


         */
int yukseklik=3;
int boy=4;

        for (int i =1; i <=yukseklik ; i++) {
            for (int j =1; j <=boy ; j++) {
                System.out.print("* ");
            }System.out.println("  ");

        }



    }
}
